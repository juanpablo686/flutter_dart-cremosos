/// Funciones de utilidad para filtrar productos
Product? getProductById(String id) {
  try {
    return allProducts.firstWhere((product) => product.id == id);
  } catch (e) {
    return null;
  }
}

List<Product> getProductsByCategory(ProductCategory category) {
  return allProducts.where((product) => product.category == category).toList();
}

List<Product> getFeaturedProducts() {
  return allProducts.where((product) => product.featured == true).toList();
}

List<Product> getProductsOnSale() {
  return allProducts.where((product) => product.onSale == true).toList();
}

List<Product> searchProducts(String query) {
  final searchTerm = query.toLowerCase();
  return allProducts
      .where((product) =>
          product.name.toLowerCase().contains(searchTerm) ||
          product.description.toLowerCase().contains(searchTerm) ||
          product.ingredients.any(
              (ingredient) => ingredient.toLowerCase().contains(searchTerm)))
      .toList();
}
